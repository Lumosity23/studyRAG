// Simple icon replacement system
window.lucide = {
  createIcons: function() {
    // Find all elements with data-lucide attribute
    const iconElements = document.querySelectorAll('[data-lucide]');
    
    iconElements.forEach(element => {
      const iconName = element.getAttribute('data-lucide');
      const iconSvg = ICONS[iconName];
      
      if (iconSvg) {
        // Create a temporary div to parse the SVG
        const tempDiv = document.createElement('div');
        tempDiv.innerHTML = iconSvg;
        const svgElement = tempDiv.firstChild;
        
        // Copy classes and styles from the original element
        if (element.className) {
          svgElement.setAttribute('class', element.className);
        }
        
        // Copy any inline styles
        if (element.style.cssText) {
          svgElement.style.cssText = element.style.cssText;
        }
        
        // Replace the element
        element.parentNode.replaceChild(svgElement, element);
      } else {
        console.warn(`Icon "${iconName}" not found`);
        // Fallback: create a simple square
        element.innerHTML = '<div style="width: 1em; height: 1em; background: currentColor; border-radius: 2px;"></div>';
      }
    });
  }
};

// Auto-initialize icons when DOM is loaded
if (document.readyState === 'loading') {
  document.addEventListener('DOMContentLoaded', () => {
    window.lucide.createIcons();
  });
} else {
  window.lucide.createIcons();
}